uint32_t double_to_float(uint64_t a) {
	uint32_t z = 0;
	uint32_t z_e = 0;
	uint32_t z_m = 0;
	uint32_t sticky = 0;
	uint32_t guard = 0;
	uint32_t round = 0;

	/*
	 * sign = a[63]
	 * exponent = a[62:52]
	 * fraction = a[51:0]
	 */

	/* Sign bit */
	if (a & ((uint64_t)1<<63)) {
		z |= (1<<31);
	}

	uint32_t a_exp = ((a >> 52) & 0x7ff);
	uint64_t a_frac = a & 0xfffffffffffff;
	uint32_t a_frac29 = a_frac >> 29;
	DEBUG_PRINTF("a_exp: %d\n", a_exp);
	DEBUG_PRINTF("a_frac29: %08x\n", a_frac29);

	if (a_exp == 0) {
		/* z is 0 except for sign bit */
	} else if (a_exp < 897) {
		/* z_exp is 0 */
		z_m = (1<<23) | (a_frac29);
		z_e = a_exp;
		guard = a & (1<<28);
		round = a & (1<<27);
		sticky = ((a & 0x7ffffff) != 0);

		/* Denormalize */
		for (;;) {
			//printf("denormalize\n");
			DEBUG_PRINTF("z_m: %08x\n", z_m);
			if (z_e == 897 || (z_m == 0 && guard == 0)) {
				DEBUG_PRINTF("guard: %d, round: %d, sticky: %d\n", guard, round, sticky);
				if (guard && (round || sticky || (z_m & 0x1))) {
					DEBUG_PRINTF("guard && (round || sticky)\n");
					z |= (z_m + 1) & 0x7fffff;
				} else {
					DEBUG_PRINTF("not rounding\n");
					z |= z_m & 0x7fffff;
				}
				break;
			} else {
				z_e = z_e + 1;
				sticky = sticky | round;
				round = guard;
				guard = z_m & 0x1;
				DEBUG_PRINTF("guard: %d, round: %d, sticky: %d\n", guard, round, sticky);
				z_m = z_m >> 1;
			}
		}
		/* End denormalize */
	} else if (a_exp == 2047) {
		z |= (255 << 23);
		if (a_frac) {
			z |= (1<<22);
		}
	} else if (a_exp > 1150) {
		z |= (255 << 23);
	} else {
		DEBUG_PRINTF("else path\n");
		DEBUG_PRINTF("exp: %d\n", (a_exp - 1023) + 127);
		uint32_t exp = ((a_exp - 1023) + 127);
		DEBUG_PRINTF("a[28] = %lu\n", a & (1<<28));
		DEBUG_PRINTF("a[27] = %lu\n", a & (1<<27));
		if ((a & (1<<28)) && ( (a & (1<<27)) || (a & 0x7ffffff) )) {
			DEBUG_PRINTF("rounding path\n");
			uint32_t frac = (a_frac29 + 1);
			if (frac & (1<<23)) {
				exp = exp + 1;
			}
			z |= (exp << 23) | (frac & 0x7fffff);
		} else if ((a & (1<<29)) && (a & (1<<28)) && ( (a&0xfffffff) == 0)) {
			DEBUG_PRINTF("special case\n");
			uint32_t frac = (a_frac29 + 1);
			if (frac & (1<<23)) {
				exp = exp + 1;
			}
			z |= (exp << 23) | (frac & 0x7fffff);
		} else {
			DEBUG_PRINTF("not rounding\n");
			z |= (exp << 23) | ((a_frac29) & 0x7fffff);
		}
	}

	return z;
}

uint64_t float_to_double(uint32_t a) {
	uint64_t z = 0;
	uint64_t z_e = 0;
	uint64_t z_m = 0;

	/* Sign bit */
	uint64_t sign = 0;
	if (a & (1 << 31)) {
		sign = (1UL << 63);
	}
	z |= sign;

	uint32_t a_exp = (a >> 23) & 0xff;
	uint64_t a_frac = a & 0x7fffff;

	z = z | (a_frac << 29);


	/*printf("a_exp = %d\n", a_exp);
	printf("a_frac = %lu\n", a_frac);*/
	if (a_exp == 0) {
		//printf("a_exp == 0\n");
		if (a & 0xffffff) {
			z_e = 897;
			z_m = a_frac << 29;
			//printf("z_m: %016lx\n", z_m);

			/* Normalize */
			for (;;) {
				//printf("z_m: %016lx\n", z_m);
				if (z_m & (1UL<<52)) {
					z = sign;
					/*printf("z: %016lx\n", z);
					printf("z_e: %ld, %08lx\n", z_e, z_e);*/
					z = z | (z_e << 52);
					//printf("z: %016lx\n", z);
					z = z | (z_m & 0xfffffffffffff);
					//printf("z: %016lx\n", z);
					break;
				} else {
					z_m = (z_m & 0xfffffffffffff) << 1;
					z_e = z_e - 1;
					//printf("z_e: %ld, %08lx\n", z_e, z_e);
				}
			}
			/* End normalize */
		}
	} else if (a_exp == 255) {
		//printf("a_exp == 255\n");
		z = z | (2047UL << 52);
	} else {
		//printf("other a_exp\n");
		//printf("z before: %016lx\n", z);
		uint64_t exp = (a_exp - 127) + 1023;
		uint64_t exp_shift = exp << 52;
		//printf("exp: %lu\n", exp<<52);
		z = z | exp_shift;
		//printf("z after: %016lx\n", z);
	}
	return z;
}

